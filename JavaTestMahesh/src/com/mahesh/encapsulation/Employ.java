package com.mahesh.encapsulation;

public class Employ {
	private String empNme;
	private int empId;
	private float empSalary;
	private String empAddress;
	private int empBatch;
	private int empAge;
	private String empGender;
	private int empMobileNo;

	public int getEmpMobileNo() {
		return empMobileNo;
	}

	public void setEmpMobileNo(int empMobileNo) {
		this.empMobileNo = empMobileNo;
	}

	public String getEmpNme() {
		return empNme;
	}

	public void setEmpNme(String empNme) {
		this.empNme = empNme;
	}

	public int getEmpId() {
		return empId;
	}

	public void setEmpId(int empId) {
		this.empId = empId;
	}

	public float getEmpSalary() {
		return empSalary;
	}

	public void setEmpSalary(float empSalary) {
		this.empSalary = empSalary;
	}

	public String getEmpAddress() {
		return empAddress;
	}

	public void setEmpAddress(String empAddress) {
		this.empAddress = empAddress;
	}

	public int getEmpBatch() {
		return empBatch;
	}

	public void setEmpBatch(int empBatch) {
		this.empBatch = empBatch;
	}

	public int getEmpAge() {
		return empAge;
	}

	public void setEmpAge(int empAge) {
		this.empAge = empAge;
	}

	public String getEmpGender() {
		return empGender;
	}

	public void setEmpGender(String empGender) {
		this.empGender = empGender;
	}
}
